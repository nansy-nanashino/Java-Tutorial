public class MyApp {

  public static void main(String[] args) {
    // 文字
    //文字列は"" 文字は''
    char a = 'a';
    // 整数 byte short int long 右に行くほど大きい数字を扱える（通常はint）
    int x = 10;
    //long を扱う場合最後にLをつける
    long y = 5555555555L;
    // 浮動小数点数 float double
    double d = 23423.344;
    //froat の場合最後にFをつける
    float f = 32.33F;
    // 論理値
    boolean flag = true; // false
    // 文字列
    // \n　改行
    // \t　タブ
    String msg = "Hello Wo\nrld Again\t!";
    System.out.println(msg);
  }

}
